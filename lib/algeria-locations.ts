// Export function to get communes by wilaya
export const getCommunesByWilaya = (wilaya: string): string[] => {
  return communesByWilaya[wilaya] || []
}

// Export all communes as a flat array
export const getAllCommunes = (): string[] => {
  return Object.values(communesByWilaya).flat()
}

// Export the communes data
export const algerianLocations = wilayas.map((wilaya) => ({
  wilaya,
  communes: getCommunesByWilaya(wilaya),
}))

// Helper function to search locations
export const searchLocations = (query: string): { wilaya: string; communes: string[] }[] => {
  const lowerQuery = query.toLowerCase()
  return algerianLocations.filter(
    (location) =>
      location.wilaya.toLowerCase().includes(lowerQuery) ||
      location.communes.some((commune) => commune.toLowerCase().includes(lowerQuery)),
  )
}
